//-------------------------------------------------------------------------
// min_caml_start を呼び出すためのスタブ
//-------------------------------------------------------------------------
    .text
    .globl _start
_start:
    // スタックポインタの開始アドレス (0x4F000000 ~ 0x4FFFFFFF)
    li a0, 0x4F000000

    // ヒープポインタの開始アドレス (0x4E000000 ~ 0x4EFFFFFF)
    li a1, 0x4E000000

    call min_caml_start
loop:
    j loop


